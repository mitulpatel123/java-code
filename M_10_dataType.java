package com.company;

public class M_10_dataType {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a++);
        System.out.println(a);
        int b = 7;
        System.out.println(++b);

        int c = 6;
        System.out.println(c--);
        System.out.println(c);

        int d =10;
        System.out.println(--d);

        int x =7;
        int y = (++x)*8;
        System.out.println(y);

        char v = 'a';
        System.out.println(++v);
    }

}
